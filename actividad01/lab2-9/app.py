arreglo1 = [1, 3, 5, 7, 9]
arreglo2 = [2, 4, 6, 8, 10]

arreglo_combinado = []

for elemento in arreglo1:
    arreglo_combinado.append(elemento)

for elemento in arreglo2:
    arreglo_combinado.append(elemento)

print(f"Arreglo #1: {arreglo1}")
print(f"Arreglo #2: {arreglo2}")
print(f"El arreglo combinado es: {arreglo_combinado}")